package binky.reportrunner.exceptions;

public class ChartDefinitionException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 3066641058543892450L;

	public ChartDefinitionException() {
		super();
	}

	public ChartDefinitionException(String message, Throwable cause) {
		super(message, cause);
	}

	public ChartDefinitionException(String message) {
		super(message);
	}

	public ChartDefinitionException(Throwable cause) {
		super(cause);
	}

}
